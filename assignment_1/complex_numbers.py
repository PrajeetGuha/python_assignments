number_1 = complex( input( 'Enter the first complex number:' ) )
number_2 = complex( input( 'Enter the second complex number:' ) )

print( 'Addition:', ( number_1 + number_2 ) )
print( 'Substraction:', ( number_2 - number_1 ), '\t', ( number_1 - number_2 ) )
print( 'Multiply:', ( number_1 * number_2 ) )
print( 'Divide:', ( number_1 / number_2 ), '\t', ( number_1 / number_2 ) )